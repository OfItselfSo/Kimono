    /// <summary>
    /// .Net has a very poor mechanism for identifying the sender of a drag drop
    /// so we actually drag drop this container which contains multiple informational
    /// things
    /// </summary>
    public class MonitorBlockDragDropContainer
    {
        private object sender = null;
        private MonitorBlockProperties_Base mbObj = null;

        /// +=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=
        /// <summary>
        /// Constructor
        /// </summary>
        public MonitorBlockDragDropContainer(object senderIn, MonitorBlockProperties_Base mbObjIn)
        {
            Sender = senderIn;
            MBObj = mbObjIn;
        }

        public object Sender { get => sender; set => sender = value; }
        public MonitorBlockProperties_Base MBObj { get => mbObj; set => mbObj = value; }
    }
}
